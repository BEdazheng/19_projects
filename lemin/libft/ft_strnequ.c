#include "libft.h"

int		ft_strnequ(const char *s1, const char *s2, size_t n)
{
	if (!s1 || !s2)
		return (0);
	return (ft_strncmp((char *)s1, (char*)s2, n) == 0 ? 1 : 0);
}
