#include "libft.h"

int	ft_strchri(const char *s, int c)
{
	int i;

	i = 0;
	if (!s)
		return (-1);
	while (s[i] && s[i] != (char)c)
		i++;
	if (c == '\0' || s[i] != '\0')
		return (i);
	return (-1);
}
